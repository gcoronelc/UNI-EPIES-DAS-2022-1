   private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
      // Variables
		record = new EmpleadoModel();
		// Proceso
		try {
			// Datos
			record.setId(Integer.parseInt(txtId.getText()));
			record.setApellido(txtApellidos.getText());
			record.setNombre(txtNombres.getText());
			record.setDni(txtDni.getText());
			record.setCorreo(txtCorreo.getText());
			record.setTelefono(txtTelefono.getText());
			record.setRol(Integer.parseInt(txtRol.getText()));
			record.setUsuario(txtUsuario.getText());
			record.setClave(String.valueOf(txtClave.getPassword()));
			// Proceso
			EmpleadoCrudController control = new EmpleadoCrudController();
			switch(this.accion){
				case UtilView.CRUD_NUEVO:
					control.insert(record);
					break;
				case UtilView.CRUD_EDITAR:
					control.update(record);
					break;
				case UtilView.CRUD_ELIMINAR:
					control.delete(record.getId());
					break;
			}
			// Cerrar formulario
			Session.put("bean", record);
			this.dispose();
			Mensaje.info(this, "Proceso ejecutado correctamente.");
		} catch (Exception e) {
			Mensaje.error(this, e.getMessage());
		}
   }//GEN-LAST:event_btnGrabarActionPerformed

