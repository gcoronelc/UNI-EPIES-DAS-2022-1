	public void setAccion(String accion) {
		this.accion = accion;
		this.setTitle(accion + " Empleado");
		this.setEditStatus();
	}
	
	private void setEditStatus(){
		txtApellidos.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtNombres.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtDni.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtTelefono.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtCorreo.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtUsuario.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtRol.setEnabled(!accion.equals(UtilView.CRUD_ELIMINAR));
		txtClave.setEnabled(accion.equals(UtilView.CRUD_NUEVO));
	}
	
	public void setRecord(EmpleadoModel record){
		this.record = record;
		pintarRecord();
	}
	
	private void pintarRecord(){
		txtId.setText(record.getId() + "" );
		txtApellidos.setText(record.getApellido());
		txtNombres.setText(record.getNombre());
		txtDni.setText(record.getDni());
		txtTelefono.setText(record.getTelefono());
		txtCorreo.setText(record.getCorreo());
		txtRol.setText(record.getRol() + "");
		txtUsuario.setText(record.getUsuario());
		txtClave.setText(record.getClave());
	}

